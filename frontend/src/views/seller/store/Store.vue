<template>
    <div id="Store">
        <header-store :storeName=store.storeName :city=store.city />
        <product-store :listProduct=store.listProduct />
    </div>
</template>

<script>
import HeaderStore    from './components/HeaderStore.vue'
import ProductStore   from './components/ProductStore.vue'
import DataViewSidebar from './components/DataViewSidebar.vue'
import moduleDataList from '@/store/data-list/moduleDataList.js'
import apiUser from '../../../api/user'

export default {
  components: {
    HeaderStore,
    ProductStore,
    DataViewSidebar
  },
  data () {
    return {
      selected: [],
      store: {},
      product: [],
      itemsPerPage: 4,
      isMounted: false,
      addNewDataSidebar: false,
      sidebarData: {}
    }
  },
  computed: {
    currentPage () {
      if (this.isMounted) {
        return this.$refs.table.currentx
      }
      return 0
    },
    products () {
      return this.$store.state.dataList.products
    },
    queriedItems () {
      return this.$refs.table ? this.$refs.table.queriedResults.length : this.products.length
    }
  },
  methods: {
    addNewData () {
      this.sidebarData = {}
      this.toggleDataSidebar(true)
    },
    deleteData (id) {
      this.$store.dispatch('dataList/removeItem', id).catch(err => { console.error(err) })
    },
    editData (data) {
      // this.sidebarData = JSON.parse(JSON.stringify(this.blankData))
      this.sidebarData = data
      this.toggleDataSidebar(true)
    },
    getOrderStatusColor (status) {
      if (status === 'on_hold')   return 'warning'
      if (status === 'delivered') return 'success'
      if (status === 'canceled')  return 'danger'
      return 'primary'
    },
    getPopularityColor (num) {
      if (num > 90)  return 'success'
      if (num > 70)  return 'primary'
      if (num >= 50) return 'warning'
      if (num < 50)  return 'danger'
      return 'primary'
    },
    toggleDataSidebar (val = false) {
      this.addNewDataSidebar = val
    }
  },
  created () {
    if (!moduleDataList.isRegistered) {
      this.$store.registerModule('dataList', moduleDataList)
      moduleDataList.isRegistered = true
    }
    this.$store.dispatch('dataList/fetchDataListItems')
  },
  mounted () {
    // this.isMounted = true
    console.log(localStorage.getItem('userId'))
    apiUser
      .GetStoreByIdWithProduct(localStorage.getItem('userId'))
      .then((response) => { 
        console.log(response) 
        this.store = response 
      })
      .catch((error) => { console.log('Error get all data product!', error) })
  }
}
</script>

